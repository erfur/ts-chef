import { AddTextToImage } from "../../src/chef/operations/AddTextToImage";
import { strToAB } from "../helpers";

describe("AddTextToImage", () => {
  const op = new AddTextToImage();

  test("Invalid file type", async () => {
    const input = strToAB("not an image");
    const args = [
      "text",
      "None",
      "None",
      0,
      0,
      32,
      "Roboto",
      255,
      255,
      255,
      255,
    ];
    await expect(op.run(input, args)).rejects.toThrow("Invalid file type.");
  });

  test("present() method returns HTML image tag", () => {
    // Small 1x1 PNG
    const input = new Uint8Array([
      0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, 0x00, 0x00, 0x00, 0x0d,
      0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
      0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53, 0xde, 0x00, 0x00, 0x00,
      0x0c, 0x49, 0x44, 0x41, 0x54, 0x08, 0xd7, 0x63, 0xf8, 0xff, 0xff, 0x3f,
      0x00, 0x05, 0xfe, 0x02, 0xfe, 0xdc, 0x44, 0x74, 0x06, 0x00, 0x00, 0x00,
      0x00, 0x49, 0x45, 0x4e, 0x44, 0xae, 0x42, 0x60, 0x82,
    ]).buffer;
    const result = op.present(input);
    expect(result).toContain('<img src="data:image/png;base64,');
  });

  test("present() with empty input", () => {
    expect(op.present(new ArrayBuffer(0))).toBe("");
  });
});
